class Gallery extends Component {
  state = {imgUrl: imagesList[0].imageUrl, imgAlt: imagesList[0].imageAltText}

  onChangeImage = (newImage, newAlt) => {
    this.setState({imgUrl: newImage, imgAlt: newAlt})
  }

  render() {
    const {imgUrl, imgAlt} = this.state
    return (
      <div className="container">
        <img src={imgUrl} alt={imgAlt} className="big-img" />
        <div className="names-container">
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
        </div>
        <ul className="ul-container">
          {imagesList.map(eachImage => (
            <ThumbnailItem
              imageDetails={eachImage}
              key={eachImage.id}
              onChangeImage={this.onChangeImage}
              isActive={imgUrl === eachImage.imageUrl}
            />
          ))}
        </ul>
      </div>
    )
  }
}
